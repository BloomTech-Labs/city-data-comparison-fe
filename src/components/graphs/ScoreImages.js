export function scoreStatement(score) {
  if (score <= 30) {
    return "Inadequate";
  } else if (score >= 30 && score <= 55) {
    return "Minimal"
  } else if (score >= 55 && score <= 80) {
    return "Good"
  } else if (score > 80) {
    return "Excellent"
  }
};

